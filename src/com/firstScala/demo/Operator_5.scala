package com.firstScala.demo

/**
 * @author fujh
 *  5、Scala运算符
 *    1、算数运算符
 *    2、关系运算符
 *    3、逻辑运算符
 *    4、位运算符号
 *    5、赋值运算符
 */
object Operator_5 {

  def main(args: Array[String]): Unit = {
    //1、算数运算符
    println("算数运算符：")
    var a = 10
    var b = 20
    println("a+b="+(a+b))
    println("a-b="+(a-b))
    println("a*b="+(a*b))
    println("a/b="+(a/b))
    println("a%b="+(a%b))

    //2、关系原算符
    println("关系运算符：")
    println("a>b="+(a>b))
    println("a<b="+(a<b))
    println("a==b="+(a==b))
    println("a>=b="+(a>=b))
    println("a<=b="+(a<=b))
    println("a!=b="+(a!=b))

    //3、逻辑运算符
    println("逻辑运算符：")
    println("与：&&")
    println("或：||")
    println("非：！")

    //4、位运算符
    println("位运算符：")
    println("不了解啦！")

    //5、赋值运算符
    println("赋值运算符：")
    println("赋值：=")
    println("累加：+=")
    println("累减：-=")
    println("等等")

  }

}
